use anyhow::Result;
use assert_cmd::assert::OutputAssertExt;
use assert_fs::prelude::*;

use uv_static::EnvVars;
use uv_test::uv_snapshot;

/// Read-only project commands should not require an initialized cache.
#[test]
fn read_only_project_commands_skip_cache_initialization() -> Result<()> {
    let context = uv_test::test_context!("3.12");

    context.temp_dir.child("pyproject.toml").write_str(
        r#"
            [project]
            name = "project"
            version = "0.1.0"
            requires-python = ">=3.12"
            "#,
    )?;

    // Create the initial lockfile with a valid cache.
    context.lock().assert().success();

    // A file can't be initialized as a cache directory.
    let cache_file = context.temp_dir.child("cache-file");
    cache_file.touch()?;

    uv_snapshot!(context.filters(), context.lock()
        .arg("--frozen")
        .env(EnvVars::UV_CACHE_DIR, cache_file.as_os_str()), @"
    success: true
    exit_code: 0
    ----- stdout -----

    ----- stderr -----
    warning: The lockfile at `uv.lock` was only checked for validity, not whether it is up-to-date, because `--frozen` was provided; use `--check` instead
    ");

    uv_snapshot!(context.filters(), context.export()
        .arg("--frozen")
        .env(EnvVars::UV_CACHE_DIR, cache_file.as_os_str()), @"
    success: true
    exit_code: 0
    ----- stdout -----
    # This file was autogenerated by uv via the following command:
    #    uv export --cache-dir [CACHE_DIR] --frozen

    ----- stderr -----
    ");

    uv_snapshot!(context.filters(), context.tree()
        .arg("--frozen")
        .arg("--universal")
        .env(EnvVars::UV_CACHE_DIR, cache_file.as_os_str()), @"
    success: true
    exit_code: 0
    ----- stdout -----
    project v0.1.0

    ----- stderr -----
    ");

    Ok(())
}
